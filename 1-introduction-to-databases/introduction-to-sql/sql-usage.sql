-- SQL SUBSETS
-- 1. DDL (Data Definition Language) : create data
    -- DLL CREATE COMMAND -> used to create storage objects in db, like tables
    -- DDL ALTER COMMAND -> used to modify the stucture of a table object in a db
    -- DDL DROP COMMAND -> removed an existing object from a db


-- 2. DML (Data Manipulation Language): insert, update, delete data
    --  DML INSERT COMMAND -> insert records of data into a database table
    --  DML UPDATE COMMAND -> edit data that already exists in a database table
    --  DML DELETE COMMAND -> delete one or more rows of data from a table


-- 3. DQL (Data Query Language) : read or retrieve data
-- DQL SELECT COMMAND -> retrieve data


-- 4. DCL (Data Control Language) : control access to db
    --  GRANT ACCESS
    --  REVOKE ACCESS 

