-- What is database structure?

-- 1. Tables or entities, where the data is stored. 
-- 2. Attributes which are details about the table or entity. In other words, attributes describe the table.  
-- 3. Fields, which are columns used to capture attributes. 
-- 4. A record, which is one row of details about a table or entity. 
-- 5. And the primary key, which is a unique value for an entity. 

---------------------------------------------------------------------------------------------------------------------------------------------------

-- Data types: 

-- 1. Numeric data types such as INT, TINYINT, BIGINT, FLOAT and REAL. 

-- 2. Date and time data types such as DATE, TIME and DATETIME. 

-- 3. Character and string data types such as CHAR and VARCHAR.

-- 4. Binary data types such as BINARY and VARBINARY. 

-- 5. And miscellaneous data types such as: 
        -- a. Character Large Object (CLOB), for storing a large block of text in some form of text encoding.  
        -- b. and Binary Large Object (BLOB), for storing a collection of binary data such as images. 

