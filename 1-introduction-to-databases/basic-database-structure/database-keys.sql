-- identify the main keys in tables in a rdb
-- relationship between keys in a table

-- 1. KEY ATTRIBUTE -> used to uniquely identify an individual record of data
    -- a. Candidate key attribute -> any attribute that contains a unique value in each row of table
    -- b. Composite key attribute -> a key composed of two or more atributes to form a unique value in each new row
    -- c. Primary key -> a selected candidate key
    -- d. Alternate key -> a candidate key not selected as the primary key
    -- e. Foreign Key -> an attribute that references a unique key in another table