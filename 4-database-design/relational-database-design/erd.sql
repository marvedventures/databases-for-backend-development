--------------------------------------------------------------------------------------------------------------------------------------------------
-- Entity Relationship Diagram (ERD)
--------------------------------------------------------------------------------------------------------------------------------------------------

-- 1. Entity representation: 

-- In the ER-D, a box with two compartments is used to represent the entity and its related attributes. 
-- The top compartment represents the entity name, and the bottom compartment includes the related attributes. 

--------------------------------------------------------------------------------------------------------------------------------------------------

-- 2. Relationship representation: 

-- The ER diagram uses different styles of lines to define the distinct types of relationships between entities. 
-- The line style depends on the cardinality of the relationship in the following three cases :
--          -> a. 1:1 (one-to-one):   The ER-D uses a straight-line 
--          -> b  1:N (one-to-many):  The ER-D is a straight line with a crow’s foot notation on one side only 
--          -> c. M:N (many-to-many): The ER-D is a straight line with crow’s foot notations on both sides of entities

--------------------------------------------------------------------------------------------------------------------------------------------------

-- 3. Attributes representation :
-- Each entity has a set of attributes that hold relevant information about it. Each attribute must be defined with a data type