--------------------------------------------------------------------------------------------------------------------------------------------------
-- DATABASE SCHEMA
--------------------------------------------------------------------------------------------------------------------------------------------------

-- Schema : organization of information and its relationships

-- Database Schema -> blueprint of a database
-- MySQL : collection of data structures within a database
-- SQL Server : collection of individual, but related components
-- PostgreSQL : a namespace with named database objects
-- Oracle : Property of each respective database user

--------------------------------------------------------------------------------------------------------------------------------------------------

-- PARTS OF DATABASE SCHEMA

-- 1. Data relationships
-- 2. Unique object keys
-- 3. Name and data types

--------------------------------------------------------------------------------------------------------------------------------------------------

-- DATABASE SCHEMA CATEGORIES

-- 1. Conceptual / Logical Schema : defines entities, attributes and relationships (Entity Relationship Diagram)
-- 2. Internal / Physical Schema : defines how data is stored in a secondary storage (Code itself)
-- 3. External / View Schema : defines different user views

--------------------------------------------------------------------------------------------------------------------------------------------------

-- ADVATANGES OF A DATABASE SCHEMA
-- 1. Management : provide logical groupings for objects
-- 2. Accessibility : enable greater accessibility to objects
-- 3. Security : offer a range of useful security features
-- 4. Ownership : permit transfer of ownership between users